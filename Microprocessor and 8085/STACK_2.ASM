	MOV	AX,0
	MOV	SS,AX
	MOV	DS,AX
	MOV	SP,0540H
	;
	MOV	CX,16
	MOV	SI,2000H
L1:	CALL	SHIFT
	LOOP	L1
	INT 	3
	;
SHIFT:  PUSH	CX
	MOV	AL,[SI]
	MOV	CX,4
