;*****************************************
;     MDA-Win8086 EXPERIMENT PROGRAM    *
;     FILENAME  : DAC.ASM
;     PROCESSOR : I8086
;     DAC TEST
;*****************************************
CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
	;
PPIC_C	EQU	1FH
PPIC	EQU	1DH
PPIB	EQU	1BH
PPIA	EQU	19H
	;	
	ORG	1000H
	MOV	AL,10000000B
	OUT	PPIC_C,AL
	;
	MOV	AL,11111111B
	OUT	PPIA,AL
	MOV	AL,11110000B
	OUT	PPIB,AL
	;
L2:	MOV	AL,00000000B
L1:	OUT	PPIC,AL
	CALL	TIMER
	INC	AL
	CMP	AL,50H
	JNE	L1
	JMP	L2		
	;
	INT	3
	;		
TIMER:	MOV	CX,1
TIMER2:	PUSH	CX
       	MOV	CX,0
TIMER1:	NOP
	LOOP	TIMER1
	POP	CX
	LOOP	TIMER2
	RET
	;
CODE	ENDS
	END


